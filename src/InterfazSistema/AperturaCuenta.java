    private void botonAperturaCuentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonAperturaCuentaMouseClicked
        if(ingresoMontoInicial.getText().trim().isEmpty() || ingresoDPIAperturaCuenta.getText().trim().isEmpty() || (!botonAhorro.isSelected() && !botonMonetaria.isSelected())){
            if(ingresoMontoInicial.getText().trim().isEmpty()){
                advertenciaCampoRC1.setVisible(true);
            }else{
                advertenciaCampoRC1.setVisible(false);
            }
            if(!botonAhorro.isSelected() && !botonMonetaria.isSelected()){
                advertenciaCampoRC2.setVisible(true);
            }else{
                advertenciaCampoRC2.setVisible(false);
            }
            if(ingresoDPIAperturaCuenta.getText().trim().isEmpty()){
                advertenciaCampoRC3.setVisible(true);
            }else{
                advertenciaCampoRC3.setVisible(false);
            }
        }else{
            advertenciaCampoRC1.setVisible(false);
            advertenciaCampoRC2.setVisible(false);
            advertenciaCampoRC3.setVisible(false);

            String tipCuen = "";
            if(botonAhorro.isSelected()){
                tipCuen = "Ahorro";
            }else if(botonMonetaria.isSelected()){
                tipCuen = "Monetaria";
            }else{
                tipCuen = "Invalida";
            }

            double montoInicial = Double.parseDouble(ingresoMontoInicial.getText());
            String tipoCuenta = tipCuen;
            String dpi = ingresoDPIAperturaCuenta.getText();


            LogicaAperturaCuenta aperturaCuenta = new LogicaAperturaCuenta(montoInicial, tipoCuenta, dpi);
            aperturaCuenta.verNombreYApellidos();
            aperturaCuenta.abrirCuenta();
            aperturaCuenta.abrirCuentaArchivo();
            
            if(aperturaCuenta.getValidacionDPIVista().equals("verdad")){
                labelNoCuenta.setVisible(true);
                labelCodigoCliente.setVisible(true);
                labelNombreAC.setVisible(true);
                labelApellidosAC.setVisible(true);
                labelFechaAperturaCuenta.setVisible(true);
                tituloNoCuenta.setVisible(true);
                tituloCodigoCliente.setVisible(true);
                tituloNombreAC.setVisible(true);
                tituloApellidosAC.setVisible(true);
                tituloFechaAperturaCuenta.setVisible(true);
            }            
            
            if(aperturaCuenta.getValidacionDPIVista().equals("verdad")){
                labelNombreAC.setText(" " + aperturaCuenta.getNombreEncontrado());
                labelApellidosAC.setText(" " + aperturaCuenta.getApellidosEncontrado());
                labelCodigoCliente.setText(" " + aperturaCuenta.getCodigoCliente());
                labelFechaAperturaCuenta.setText(" " + aperturaCuenta.getFechaApertura());
                labelNoCuenta.setText(" " + aperturaCuenta.getNoCuenta());
            }
            advertenciaExistenciaDPI.setText(aperturaCuenta.getAvisoExistenciaDpi());
            
        }
    }//GEN-LAST:event_botonAperturaCuentaMouseClicked

